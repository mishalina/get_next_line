#include "libft.h"

char		*ft_strncat(char *d, const char *s, size_t n)
{
	int		i;
	int		t;

	i = 0;
	t = 0;
	if (*s == '\0')
		return (d);
	while (d[i] != '\0')
		i++;
	while (s[t] != '\0' && t < (int)n)
	{
		d[i + t] = s[t];
		t++;
	}
	d[i + t] = '\0';
	return (d);
}
