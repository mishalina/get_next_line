#include "libft.h"

int		ft_toupper(int ch)
{
	if ((65 <= ch && ch <= 90) || (97 <= ch && ch <= 122))
	{
		if (97 <= ch && ch <= 122)
		{
			ch = ch - 32;
		}
		return (ch);
	}
	else
		return (ch);
}
