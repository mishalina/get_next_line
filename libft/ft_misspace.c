#include "libft.h"

int		ft_misspace(const char *str)
{
	int		i;
	char	*s;

	i = 0;
	s = (char*)str;
	while (s[i] == '\t' || s[i] == ' ' || s[i] == '\v' ||
			s[i] == '\r' || s[i] == '\f' || s[i] == '\n')
		i++;
	return (i);
}
