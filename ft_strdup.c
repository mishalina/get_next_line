#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		len;
	char	*dup;

	len = 0;
	while (src[len] != '\0')
		len++;
	dup = (char*)malloc(sizeof(char) * (len + 1));
	i = 0;
	if (dup == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
